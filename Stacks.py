# lists make good stacks
# stacks are 'last in, first out' data structures

my_stack = [1, 2, 3, 4, 5]
# initial stack

my_stack.append(6)
my_stack.append(7)
# places 6 and then 7 on top of the stack

my_stack.pop()  # returns 7 off the top of the stack
my_stack.pop()  # returns 6 off the top of the stack